export type TranslationKey = keyof typeof EN_US;
export type TranslationVariables = Readonly<Record<string, string | number>>;

export function translate(key: string, variables: TranslationVariables = {}): string {
  const template = (EN_US as Readonly<Record<string, string>>)[key];
  if (!template) return `[[${key}]]`;
  return template.replace(/\{(\w+)\}/g, (match, name: string) =>
    Object.prototype.hasOwnProperty.call(variables, name) ? String(variables[name]) : match,
  );
}

export const t = (key: TranslationKey, variables: TranslationVariables = {}): string =>
  translate(key, variables);
