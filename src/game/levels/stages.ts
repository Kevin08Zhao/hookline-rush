function ground(id: string, from: number, to: number, y = 690): PlatformSpec {
  return { id, x: (from + to) / 2, y, width: to - from, height: 100, kind: 'solid' };
}

function platform(
  id: string,
  x: number,
  y: number,
  width: number,
  kind: PlatformSpec['kind'] = 'solid',
  extras: Partial<PlatformSpec> = {},
): PlatformSpec {
  return { id, x, y, width, height: 26, kind, ...extras };
}

