export function materializeStage(stage: StageBlueprint, seed = stage.seed): MaterializedStage {
  const random = new SeededRandom(seed);
  return {
    ...stage,
    platforms: stage.platforms.map((item) => ({
      ...item,
      periodMs: item.periodMs ? Math.round(item.periodMs * 1.12) : undefined,
    })),
    anchors: stage.anchors.map((anchor) => ({
      ...anchor,
      periodMs: anchor.periodMs ? Math.round(anchor.periodMs * 1.08) : undefined,
    })),
    hazards: stage.hazards.map((hazard) => {
      const periodMs = hazard.periodMs ? Math.round(hazard.periodMs * 1.12) : undefined;
      return {
        ...hazard,
        periodMs,
        runtimePhaseMs:
          hazard.phaseMs ?? random.int(0, Math.max(1, periodMs ?? hazard.periodMs ?? 1000)),
      };
    }),
    enemies: stage.enemies.map((enemy) => ({
      ...enemy,
      runtimePhaseMs: random.int(0, 2000),
    })),
  };
}
